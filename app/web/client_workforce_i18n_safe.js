  function lang() {
    const value = String(localStorage.getItem(LANG_KEY) || "es").toLowerCase();
    return ["es", "en", "fr"].includes(value) ? value : "es";
  }

  function norm(value) {
    return String(value || "")
      .replace(/\s+/g, " ")
      .trim()
      .toLowerCase();
  }

  function t(key) {
    return (DICT[lang()] && DICT[lang()][key]) || DICT.es[key] || key;
  }

  function shouldSkipText(value) {
    const raw = String(value || "").trim();
    if (!raw) return true;
    if (/^[\d\s.,:$%#@/_-]+$/.test(raw)) return true;
    if (raw.includes("@")) return true;
    if (/^[a-f0-9-]{20,}$/i.test(raw)) return true;
    if (/^\d{4}-\d{2}-\d{2}/.test(raw)) return true;
    return false;
  }

  function translateText(value) {
    const raw = String(value || "");
    const clean = raw.replace(/\s+/g, " ").trim();

    if (shouldSkipText(clean)) return raw;

    const showingMatch = clean.match(/^Mostrando\s+(.+?)\s+de\s+(.+?)\s+registros\.?$/i);
    if (showingMatch) {
      if (lang() === "en") return raw.replace(clean, `Showing ${showingMatch[1]} of ${showingMatch[2]} records.`);
      if (lang() === "fr") return raw.replace(clean, `Affichage ${showingMatch[1]} sur ${showingMatch[2]} enregistrements.`);
      return raw;
    }

    const companySubtitle = clean.match(/^(.+?)\s+administra su personal de forma independiente\.?$/i);
    if (companySubtitle) {
      return raw.replace(clean, `${companySubtitle[1]} ${t("moduleSubtitleSuffix")}`);
    }

    const key = ALIASES[norm(clean)];
    if (!key) return raw;

    return raw.replace(clean, t(key));
  }

